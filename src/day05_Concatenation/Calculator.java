package day05_Concatenation;

public class Calculator {
    public static void main(String[] args) {

        int firsNumber = 100;
        int secondNumber = 50;

        //100 + 50 = 150
        System.out.println(firsNumber+" + "+secondNumber+" = "+(firsNumber+secondNumber));

        //100 - 50 = 50
        System.out.println(firsNumber+" - "+secondNumber+" = "+(firsNumber-secondNumber));

        //100 * 50 = 5000
        System.out.println(firsNumber+" * "+secondNumber+" = "+(firsNumber*secondNumber));





    }
}
