package day03_EscapeSequences;

public class Println_Vs_Print2 {
    public static void main(String[] args) {

        System.out.println("Knock knock");
        System.out.println("Who is this?");

        System.out.println("This is Java");

        System.out.println("----------------");

        System.out.print("Knock Knock");
        System.out.print("Who is this?");
        System.out.print("This is Java");

        System.out.println();
        System.out.println("----------------");


        System.out.println("Hello Cydeo \nHow are you today? \nIt's nice to see you all! ");
        System.out.println("\tJava is cool programming language" );
        System.out.println("/\\");
        System.out.println("My favorite show is \"Game of Thrones\"");

        /*
        Escape sequences: Must be given with double quote
            \n: starts a new line
            \t: paragraph space(tab)
            \\: single back slash
            \": double quote

         */




    }


}
